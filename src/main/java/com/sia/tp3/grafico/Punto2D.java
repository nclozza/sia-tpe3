package com.sia.tp3.grafico;

public class Punto2D {

    private double x;
    private double y;

    public Punto2D(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
